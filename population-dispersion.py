#population dispersion for 10 six-sided die rolls

import statistics

statistics.pvariance([1, 3, 4, 2, 6, 5, 3, 4, 5, 2])

print("Variance :", statistics.pvariance([1, 3, 4, 2, 6, 5, 3, 4, 5, 2]))

statistics.pstdev([1, 3, 4, 2, 6, 5, 3, 4, 5, 2])

print("Standard Deviation :", statistics.pstdev([1, 3, 4, 2, 6, 5, 3, 4, 5, 2]))

